
# Python program to find the number of times 4 appears in the tuple. 
#Input: tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 )

#declare a tuplex
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Count the number of times 4 appears in the tuple
count = tuplex.count(4)

# Print the result
print("The number 4 appears", count, "times in the tuple.")

#output:The number 4 appears 3 times in the tuple.
